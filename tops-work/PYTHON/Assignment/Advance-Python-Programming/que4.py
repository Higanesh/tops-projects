"""
Que4. Write a Python program to read a string, an integer, and a float from
the keyboard and display them.
"""

string_value = input("Enter any string: ")
integer_value = int(input("Enter any integer value: "))
float_value = float(input("Enter any float value: "))

print(f"String value: {string_value}")
print(f"Integer value: {integer_value}")
print(f"Float value: {float_value}")